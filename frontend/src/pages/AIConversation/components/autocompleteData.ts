/**
 * 根据输入的部分字符获取建议
 */
export function getSuggestions(input: string, type: 'school' | 'major'): string[] {
  const list = type === 'school' ? COMMON_UNIVERSITIES : COMMON_MAJORS
  const lowerInput = input.toLowerCase()
  
  return list
    .filter(item => 
      item.toLowerCase().includes(lowerInput) || 
      item.includes(input)
    )
    .sort((a, b) => {
      // 优先显示前缀匹配的结果
      const aStartsWith = a.toLowerCase().startsWith(lowerInput)
      const bStartsWith = b.toLowerCase().startsWith(lowerInput)
      if (aStartsWith && !bStartsWith) return -1
      if (!aStartsWith && bStartsWith) return 1
      return a.localeCompare(b)
    })
}

